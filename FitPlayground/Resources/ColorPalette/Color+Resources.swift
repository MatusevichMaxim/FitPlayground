import SwiftUI

extension Color {
    // MARK: - Base+Tints
    public static let appBg = Color(.appBg)
    public static let appPrimary = Color(.appPrimary)
    public static let appSecondary = Color(.appSecondary)
    public static let appAccent = Color(.appAccent)
    
    public static let appPrimary900 = Color(.appPrimary900)
    
    // MARK: - Accents
    public static let appBlue = Color(.appBlue)
    
    public static let appGreen = Color(.appGreen)
    
    public static let appRed = Color(.appRed)
    
    // MARK: - Text
    public static let textPrimary = Color(.textPrimary)
    public static let textPrimaryReversed = Color(.textPrimaryReversed)
    public static let textSecondary = Color(.textSecondary)
    public static let textSecondaryReversed = Color(.textSecondaryReversed)
    public static let textTertiary = Color(.textTertiary)
    public static let textTertiaryReversed = Color(.textTertiaryReversed)
}
